package org.open.erp.services.vanzari;

/*
 * @author Irina Bogdan
 */

public class Vanzator {

	Integer idVinzator;
	String nume;
	String prenume;
	String adresa;
	Integer codAcces;
	
	public Integer getIdVinzator() {
		return idVinzator;
	}
	public void setIdVinzator(Integer idVinzator) {
		this.idVinzator = idVinzator;
	}
	public String getNume() {
		return nume;
	}
	public void setNume(String nume) {
		this.nume = nume;
	}
	public String getPrenume() {
		return prenume;
	}
	public void setPrenume(String prenume) {
		this.prenume = prenume;
	}
	public String getAdresa() {
		return adresa;
	}
	public void setAdresa(String adresa) {
		this.adresa = adresa;
	}
	public Integer getCodAcces() {
		return codAcces;
	}
	public void setCodAcces(Integer codAcces) {
		this.codAcces = codAcces;
	}
	
	
}
